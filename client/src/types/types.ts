/**
 * Enumeration for representing frequency reminder for Google Calendar reminders.
 */
enum ReminderFrequency {
  None = "none",
  Daily = "daily",
  Weekly = "weekly",
  Monthly = "monthly",
}

/**
 * Enumeration for representing days of the week.
 */
enum Weekday {
  Monday = 0,
  Tuesday = 1,
  Wednesday = 2,
  Thursday = 3,
  Friday = 4,
  Saturday = 5,
  Sunday = 6,
}

export { ReminderFrequency, Weekday };

